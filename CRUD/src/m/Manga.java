package m;

public class Manga {
	
	private String quadri ="";
	private String magaka="";
	private String shoujo ="";
	
	
	public String getQuadri() {
		return quadri;
	}
	
	public void setQuadri(String quadri) {
		this.quadri = quadri;
	}
	
	public String getMagaka() {
		return magaka;
	}
	
	public void setMagaka(String magaka) {
		this.magaka = magaka;
	}
	
	public String getShoujo() {
		return shoujo;
	}
	
	public void setShoujo(String shoujo) {
		this.shoujo = shoujo;
	}
	
	
}

